import UIKit

extension UIColor {

    class var HCCGreen: UIColor {
        return UIColor(red: 128/255, green: 160/255, blue: 96/255, alpha: 1.0)
    }

    class var HCCDarkGreen: UIColor {
        return UIColor(red: 98/255, green: 130/255, blue: 66/255, alpha: 1.0)
    }

    class var HCCTimeoverGreen: UIColor {
        return UIColor(red: 68/255, green: 100/255, blue: 36/255, alpha: 1.0)
    }
}
